export const listMyOrders = createServerFn({ method: "GET" })
  .middleware([requireSupabaseAuth])
  .handler(async ({ context }) => {
    const email = (context.claims?.email as string | undefined) ?? "";
    const { data, error } = await context.supabase
      .from("orders")
      .select("id, invoice_number, created_at, total, status, payment_status, fulfillment, reservation_number, pickup_code, items, customer_email")
      .or(`user_id.eq.${context.userId}${email ? `,customer_email.ilike.${email}` : ""}`)
      .order("created_at", { ascending: false })
      .limit(200);
    if (error) throw new Error(error.message);
    return data ?? [];
  });

export const getMyOrder = createServerFn({ method: "GET" })
  .middleware([requireSupabaseAuth])
  .inputValidator((d: { id: string }) => z.object({ id: z.string().uuid() }).parse(d))
  .handler(async ({ data, context }) => {
    const { data: order, error } = await context.supabase
      .from("orders")
      .select("*")
      .eq("id", data.id)
      .maybeSingle();
    if (error) throw new Error(error.message);
    if (!order) throw new Error("Order not found");
    const { data: payments } = await context.supabase
      .from("payments")
      .select("*")
      .eq("order_id", data.id)
      .order("created_at", { ascending: false });
    const { data: notifications } = await context.supabase
      .from("notifications")
      .select("kind, subject, body, created_at")
      .eq("related_id", data.id)
      .order("created_at", { ascending: false });
    const productIds = Array.from(
      new Set(
        (Array.isArray((order as { items?: unknown }).items)
          ? ((order as { items: Array<{ product_id?: string }> }).items)
          : []
        ).map((i) => i?.product_id).filter((v): v is string => typeof v === "string")
      )
    );
    let products: Array<Record<string, unknown>> = [];
    if (productIds.length) {
      const { data: prods } = await context.supabase
        .from("products")
        .select("id, name, processor, ram, storage, warranty, condition, specs")
        .in("id", productIds);
      products = (prods ?? []) as never;
    }
    return { order, payments: payments ?? [], notifications: notifications ?? [], products };
  });

