import { z } from "zod";
import type { Database } from "@/integrations/supabase/types";
import { requireSupabaseAuth } from "@/integrations/supabase/auth-middleware";
import { notifyBoth, OWNER_EMAIL } from "./notify.server";
import { assertAdmin, logAudit } from "./admin/audit.server";

const ItemSchema = z.object({
  product_id: z.string().uuid(),
  name: z.string(),
  price: z.coerce.number().nonnegative(),
  qty: z.coerce.number().int().min(1),
});

const OrderInput = z.object({
  customer_name: z.string().trim().min(2),
  customer_email: z.string().trim().email(),
  customer_phone: z.string().trim().min(7),
  fulfillment: z.enum(["delivery", "pickup"]),
  delivery_address: z.string().optional().default(""),
  notes: z.string().optional().default(""),
  delivery_lat: z.number().finite().nullable().optional(),
  delivery_lng: z.number().finite().nullable().optional(),
  delivery_note: z.string().max(500).optional().default(""),
  items: z.array(ItemSchema).min(1),
});

function gen(prefix: string, n: number) {
  const alphabet = "ABCDEFGHJKLMNPQRSTUVWXYZ23456789";
  let out = prefix;
  for (let i = 0; i < n; i++) out += alphabet[Math.floor(Math.random() * alphabet.length)];
  return out;
}

export const placeOrder = createServerFn({ method: "POST" })
  .inputValidator((data: z.infer<typeof OrderInput>) => OrderInput.parse(data))
  .handler(async ({ data }) => {
    const supabase = createClient<Database>(
      process.env.SUPABASE_URL!,
      process.env.SUPABASE_SERVICE_ROLE_KEY!,
      { auth: { persistSession: false, autoRefreshToken: false } },
    );
    const subtotal = data.items.reduce((s, it) => s + it.price * it.qty, 0);
    const total = subtotal; // no tax/shipping for v1
    const isPickup = data.fulfillment === "pickup";
    const reservation_number = isPickup ? `FCS-${gen("", 6)}` : null;
    const pickup_code = isPickup ? gen("", 4) : null;

    // Anonymous checkout is supported; signed-in customers can later view
    // these orders via the email-match RLS policy on `orders`.
    const userId: string | null = null;

    const { data: order, error } = await supabase
      .from("orders")
      .insert({
        user_id: userId,
        customer_name: data.customer_name,
        customer_email: data.customer_email,
        customer_phone: data.customer_phone,
        fulfillment: data.fulfillment,
        delivery_address: data.delivery_address ?? "",
        delivery_lat: data.delivery_lat ?? null,
        delivery_lng: data.delivery_lng ?? null,
        delivery_note: data.delivery_note ?? "",
        items: data.items as never,
        subtotal,
        total,
        notes: data.notes ?? "",
        status: "pending",
        payment_status: "unpaid",
        reservation_number,
        pickup_code,
      })
      .select("id, reservation_number, pickup_code, invoice_number")
      .single();
    if (error) throw new Error(error.message);
    // Backfill invoice number if not generated by default
    let invoice_number = order.invoice_number;
    if (!invoice_number) {
      invoice_number = `INV-${order.id.replace(/-/g, "").slice(0, 10).toUpperCase()}`;
      await supabase.from("orders").update({ invoice_number }).eq("id", order.id);
    }

    // Decrement stock
    for (const it of data.items) {
      const { data: prod } = await supabase.from("products").select("stock").eq("id", it.product_id).single();
      const newStock = Math.max(0, (prod?.stock ?? 0) - it.qty);
      await supabase.from("products").update({ stock: newStock }).eq("id", it.product_id);
      await supabase.from("inventory_movements").insert({
        product_id: it.product_id, delta: -it.qty, reason: "order", reference_id: order.id, admin_id: null,
      });
    }

    // Email + notification trail
    const summary = data.items.map((it) => `• ${it.name} × ${it.qty}`).join("\n");
    await notifyBoth(supabase, {
      customerEmail: data.customer_email,
      customerUserId: userId,
      kind: "order-placed",
      adminSubject: `New order ${invoice_number} — KES ${total.toLocaleString()}`,
      adminBody: `New order from ${data.customer_name} (${data.customer_email}, ${data.customer_phone}).\nFulfillment: ${data.fulfillment}${isPickup ? ` (Reservation ${reservation_number}, code ${pickup_code})` : ""}\n\n${summary}\n\nTotal: KES ${total.toLocaleString()}`,
      customerSubject: `We received your order ${invoice_number}`,
      customerBody: `Hi ${data.customer_name},\n\nThanks for your order with Favour Computer Services.\nYour reference is ${invoice_number}.\n\n${summary}\n\nTotal: KES ${total.toLocaleString()}\n\nNext step: upload your proof of payment from your account so we can verify and prepare your order.\n\nFavour Computer Services\n0726 548 592 — F&F Building, Shop U13, Nairobi`,
      relatedType: "order",
      relatedId: order.id,
    });

    return { ...order, invoice_number };
  });

